public class C3_Display_Patterns {
  public static void main(String[] args) {
    System.out.println("*");
    System.out.println("* *");
    System.out.println("* * *");
    System.out.println("* * * *");
    System.out.println("* * * * *");

    System.out.println();

    System.out.println("        *");
    System.out.println("      * *");
    System.out.println("    * * *");
    System.out.println("  * * * *");
    System.out.println("* * * * *");

    System.out.println();

    System.out.println("* * * * *");
    System.out.println("* * * *");
    System.out.println("* * *");
    System.out.println("* *");

    
    System.out.println("*");
  }
}
